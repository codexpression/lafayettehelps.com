<h2 id="purpose">Purpose</h2>

<p><strong><em>We exist to pair people in need with people who want to help and thereby build healthy relationships and help solve the underlying causes of poverty.</em></strong></p>

<p>Remember, the entire purpose of this site is to give the people of Lafayette who have a need a place to share their story and ask for help. Just because someone has hit some hard times doesn&#8217;t mean they should be disregarded or looked down on. We believe every single person is of the highest value and when one person gives a helping hand to another, it is a beautiful thing.</p>

<p>However, we also know that people sometimes attempt to take advantage of others. It&#8217;s sad but true that sometimes one person will find a way to game the system and get an advantage at someone else&#8217;s expense. We definitely believe it&#8217;s <strong>not</strong> a beautiful thing when that happens.</p>

<p>So therefore, the purpose of this site is to both <strong>help people in need</strong> and also <strong>prevent people from abusing the goodwill of others</strong>.</p>

<p>How do we do that? I&#8217;m glad you asked.</p>

<h2 id="it8217s_about_relationships_and_reputation">It&#8217;s about Relationships and Reputation</h2>

<p>This site is based on the assumption that <strong><em>the ultimate solution to any personal problem is healthy relationships</em></strong>. It&#8217;s who you know, not what you know that makes the biggest difference, and we believe that everyone needs healthy relationships with supportive friends, and a community of faith that can meet the emotional and spiritual needs beyond the merely material needs.</p>

<p>Of course, we are not ashamed to say that our perspective is primarily Christian. We believe the perfect example of what we are talking about is the life lived by Jesus. His life is the model, the inspiration, and the power behind what we are trying to do in our day and in this city.</p>

<p>Even so, this is not a &#8220;Christian&#8221; website. You don&#8217;t have to share our beliefs to receive help or to offer help, but you should know that as you enter into a relationship with the people on this site, you just might find yourself being more attracted to the message and the methods of Jesus himself&#8230; at least, that&#8217;s partially our hope.</p>

<h2 id="okay8230_give_me_details">Okay&#8230; Give me Details!</h2>

<p>Four groups of people will use this site:</p>

<ul>
<li>People who need some practical help financially or in other ways.</li>
<li>People who want to be advocates for those in need but can&#8217;t provide specific help at the present time.</li>
<li>People with the means and desires to help people in need.</li>
<li>People who administer organizations offering practical, emotional, and/or spiritual support.</li>
</ul>

<p>If you are one of those people, we want you to register an account with the site and start getting involved.</p>

<h3 id="for_everyone8230">For everyone&#8230;</h3>

<h4 id="1_we_use_real_names">1. We use real names.</h4>

<p>You need to know first of all that this site is based on real names of real people because we are all about facilitating real relationships. If we find out that a person has falsified information, the site administrators reserve the right to ban that user from our system.</p>

<h4 id="2_last_names_are_never_shown_on_public_pages">2. Last names are never shown on public pages.</h4>

<p>Even though we are all about real relationships, we know the Internet is an easily abused community, and we want to make sure no one is made vulnerable in some way by what goes on this site. Specifically, a person&#8217;s request for help should not affect whether they can get a job. Therefore, we never show a person&#8217;s last name on public pages. Only approved site administrators will be able to see people&#8217;s last names.</p>

<h4 id="3_we_keep_track_of_people8217s_8220reputation8221_on_the_system">3. We keep track of people&#8217;s &#8220;reputation&#8221; on the system.</h4>

<p>Every person on the site has a &#8220;reputation&#8221; score assigned to them when they register for the site, and everyone starts with the same reputation as everyone else. However, each activity on the site has the potential to affect your reputation either up or down.</p>

<p>Every time a person posts a request for help, their reputation score goes down by a small amount, but every time a person successfully helps someone else out, their reputation goes up by a significant amount. Additionally, a person&#8217;s reputation score will go up if they receive recommendations from other individuals on the site. If you feel your reputation has gone down, simply get someone you know to post a recommendation for you!</p>

<h4 id="4_the_reputation_is_always_displayed">4. The reputation is always displayed.</h4>

<p>Any time you see a person&#8217;s name on the system, there will be a little reputation score bar next to their name. This bar will be red, yellow, or green depending on their reputation score. Therefore, whenever you interact on this site, you should know that your reputation score will always show up next to your name.</p>

<h3 id="for_people_needing_help8230">For people needing help&#8230;</h3>

<h4 id="5_make_a_clear_and_honest_request">5. Make a clear and honest request.</h4>

<p>If you need help, don&#8217;t feel bad. Everyone needs help from time to time, and we are glad you are doing something about it. However, we don&#8217;t want you to make up stories just to try to convince people to help you out. We have people who are ready to help you right now and they don&#8217;t need convincing.</p>

<p>What we want from you is simple honesty.</p>

<p>You see, your <em>surface need</em> might be for some money to pay an electric bill, but it&#8217;s also possible that you have something wrong with your home or apartment, and an electrician connected to this site might be able to fix it so that your home uses less electricity and so your bills are smaller in the future.</p>

<p>The only way for that kind of connection to be made is if you share the real details of your situation.</p>

<p>If your need today is for money to pay your rent, it&#8217;s likely that you are behind in other bills too and perhaps you will be needing medication soon. Rather than making little requests every time something comes up (and thereby lowering your reputation on the site) why not just put everything down here right now. Share the real details about your situation and we might be able to help you in larger ways than you can even imagine!</p>

<h4 id="6_graciously_receive_offers_for_relationship">6. Graciously receive offers for relationship.</h4>

<p>Sometimes a person will offer to help you in a way that doesn&#8217;t seem to directly relate to your immediate need. Quite often, those offers for assistance are what you will need to prevent future needs!</p>

<p>Someone might invite you to join their church family&#8230; say yes!</p>

<p>Someone might invite you to come to a group meeting&#8230; say yes!</p>

<p>Someone might offer you financial counseling&#8230; say yes!</p>

<p>Someone might offer to pay one of your bills&#8230; say yes!</p>

<p>And always say &#8220;Thank you&#8221; too!</p>

<h3 id="for_people_who_want_to_help_but_can8217t_now8230">For people who want to help, but can&#8217;t now&#8230;</h3>

<h4 id="7_offer_what_you_can">7. Offer what you can.</h4>

<p>Often a person will be asking for money, but their real need lies deeper than the surface lack of money. It&#8217;s a small thing to offer to bring a pizza to the family in need, but it starts the work of developing relationships</p>

<p>More than that, however, you probably have a skill that could really benefit the person or family in need. If you have set up a home budget, offer to help the family in need with setting up a personal budget. If you are a church member, invite the family to join you for one of your church gatherings. If you can drive, offer to take the family in need somewhere they need to go.</p>

<h4 id="8_build_a_relationship_and_leave_a_recommendation">8. Build a relationship and leave a recommendation.</h4>

<p>One of the best ways you can help someone on this site is to build a relationship with the person in need and then leave a recommendation for that person. If other people on the site know that you have a relationship with the person making the request for help, they will be more likely to offer their help too even when you can&#8217;t.</p>

<p>Your recommendations will impact the person&#8217;s reputation on the site significantly and will accelerate their chances of receiving help.</p>

<h4 id="9_make_sure_to_subscribe_to_site_notifications">9. Make sure to subscribe to site notifications.</h4>

<p>If you can&#8217;t help now, we think there may be a time when you will be able to help someone in the future, and we want you to be on the alert for when that happens. Simply click the checkbox in your account settings to enable notifications by email or texts to your phone.</p>

<p>You can also follow us on twitter to get the latest notifications that way. (not enabled yet.)</p>

<h3 id="for_people_who_want_to_help_and_can8230">For people who want to help, and can&#8230;</h3>

<h4 id="10_make_a_pledge">10. Make a pledge.</h4>

<p>At the bottom of every request page there is a spot for you to make a pledge to help the person in need. You should note that all <strong>pledges are non-binding</strong> which means that if you make a pledge, you are not obligated to keep it, but canceling your pledge may negatively affect your reputation.</p>

<p>Now, a pledge is not necessarily an offer of money, although it may be. Pledges may also be offers to help in other ways. You may offer to mow their lawn, give them a ride, bring them a meal or something else.</p>

<p>If your pledge is for an amount of money, special rules apply.</p>

<ul>
<li>At the bottom of the page, you will see a list of all the pledges divided into monetary pledges and non-monetary pledges.</li>
<li>The total of all monetary pledges will be clearly displayed.</li>
<li>If the total pledged ever equals the amount requested, you will receive a notification by your chosen method (see your account settings) and a link to a part of the site where you can fulfill your monetary commitment.</li>
</ul>

<p>There&#8217;s one more thing to note about monetary pledges:</p>

<p><em>All gifts are processed by <a href="http://lafayettecc.org">Lafayette Community Church</a>, a 501(c)3 nonprofit organization; however, all gifts given through this site minus transaction fees are given directly to the person in need and are <strong>therefore by law not tax deductible</strong>.</em></p>

<h4 id="11_request_confirmation_for_non_monetary_gifts">11. Request confirmation for non-monetary gifts.</h4>

<p>If you gave money through the site, your pledge will update to indicate that it was &#8220;completed&#8221; and your site reputation will increase by a significant amount regardless of the amount of your donation, but if you helped out in some other way, you should ask the person you helped to go back to this site and confirm what you did.</p>

<h3 id="for_people_who_administer_organizations8230">For people who administer organizations&#8230;</h3>

<h4 id="12_submit_your_organization">12. Submit your organization.</h4>

<p>If you are an administrator at a local church or other social agency, we want to know you are partnering with us in our efforts. Here&#8217;s how you get your organization on the site. </p>

<p>First, search for your organization, and if it isn&#8217;t already on the site, submit it using the links in the menu above.</p>

<p>When you submit a new organization, you will automatically be marked as the organizational admin for that organization, but it won&#8217;t be official until it is approved by the site administrators who may call you to verify your involvement with that organization.</p>

<h4 id="13_keep_your_relationships_up_to_date">13. Keep your relationships up to date.</h4>

<p>The organizational component of this site assumes that organizational administrators will visit the site frequently to do three essential things.</p>

<ul>
<li>If your relationship with someone on the site changes, we want to know it.</li>
<li>If someone on the site has no organizational relationship at all, we want you to adopt them.</li>
<li>If you have learned some new information about a person that should affect their reputation on this site, we want to know it either through the organizational &#8220;red flag&#8221; system or through the standard recommendation process.</li>
</ul>

<p>It&#8217;s also the job of the organizational administrators to add and remove other organizational administrators.</p>

